    private void autodisplay2(){                                                                        //display Login Log 
        try
        {
            tm = (DefaultTableModel)LoginLog.getModel();                                                                                   //use this table to display data
            BufferedReader br = new BufferedReader(new FileReader("Login Log.txt"));                                                             //read data from text file
            String line = "";
            while ((line = br.readLine()) != null) {     
                System.out.println(line);
                String[] data = line.split(",");                                                                                                   //check text file got data or not
                String[] nani = {data[1],data[3]};
                tm.addRow(nani);                                                                                                                   //add each line of data from text file to new row in table
            }
            br.close();                                                                                                                            //finish load data close reader
        }
        catch (IOException e)
        {
            e.printStackTrace();
        }
    }
    
