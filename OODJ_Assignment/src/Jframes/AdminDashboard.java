    private void countsupplier(){
        try{
            File file = new File("Supplier.txt");
            if(file.exists()){
                FileReader fr = new FileReader(file);
                LineNumberReader LNR = new LineNumberReader(fr);
                int linenumber = 0;
                while(LNR.readLine() != null){
                 linenumber++;
                }
                jLabel21.setText(String.valueOf(linenumber));
                fr.close();
            }
            else{
                JOptionPane.showMessageDialog(null,"File not exist.");
            }
        } catch (IOException e) { 
        }
    }
    
