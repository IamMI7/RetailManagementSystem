    private void filterCategoryTable(String queryCat){
       DefaultTableModel model = (DefaultTableModel)CategoryTable.getModel();
       TableRowSorter<DefaultTableModel> querysort = new TableRowSorter<DefaultTableModel>(model);
       CategoryTable.setRowSorter(querysort);
       querysort.setRowFilter(RowFilter.regexFilter(queryCat));
    }
    private void removeCAT(String CatFilePath, String catRemoveTerm) {
        String tempcatFile = "TempProductCategoryData.txt";
        File oldcatFile = new File(CatFilePath);
        File newcatFile = new File(tempcatFile);
        String CatID = ""; String CatName = "";
        try
        {
            FileWriter fw = new FileWriter(tempcatFile, true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            Scanner sc = new Scanner(oldcatFile);
            
            sc.useDelimiter(",");
            
            while(sc.hasNext())
            {
                CatID = sc.next();
                CatName = sc.nextLine();
                if (!CatID.equals(catRemoveTerm))
                {
                    pw.println(CatID + CatName);
                }
            }
            pw.flush();
            pw.close();
            bw.close();
            fw.close();
            sc.close();
            oldcatFile.delete();
            newcatFile.renameTo(new File("ProductCategoryData.txt"));
            JOptionPane.showMessageDialog(null,"Product Removed");
            clearCategoryTable();
            displayCategoryTable();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
            
        }
    }
    private void generateCategoryID (){
    try
    {
        File file = new File ("ProductCategoryData.txt");
        Scanner sc = new Scanner(file);
        sc.useDelimiter(",");
        if(sc.hasNext())
        {
            String ID ="";
            while(sc.hasNext()){
                ID = sc.next();
                sc.nextLine();
        }
        String ID2 = ID.replace("CID","");         //CustomerID pass to ID and replace the words "CID" into "" and pass to ID2
        int ID3 = (Integer.parseInt(ID2))+1;        //Convert ID2 into int and + 1 and pass to ID3
        String ID4 = "CID"+String.valueOf(ID3);     //Convert ID3 into string and add "CID" and pass to ID4
        PMCategoryIDTextBox.setText(ID4);
        sc.close();
    }
        else
        {
            PMCategoryIDTextBox.setText("CID1");
        }
}
    catch (Exception e)
            {
                e.printStackTrace();
            }

}
