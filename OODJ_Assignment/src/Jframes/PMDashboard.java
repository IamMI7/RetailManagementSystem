    private void getProfileDetails(){
        String checkfile = "ProductManager.txt";
        File file = new File (checkfile);
        String currentitem = PMIDTextBox.getText();
        try (Scanner sc = new Scanner(file))
        {
            sc.useDelimiter(",");
            while (sc.hasNext()){
                String ID = "";
                String Name = "";
                String IC = "";
                String Contact = "";
                String Email = "";
                
                ID = sc.next();
                Name = sc.next();
                IC = sc.next();
                sc.next();
                Contact = sc.next();
                Email = sc.next();
                sc.nextLine();

                if (ID.equals(currentitem))
                {
                    PMINTextBox.setText(IC);
                    PMUsernameTextBox.setText(Name);
                    PMContactTextBox.setText(Contact);
                    PMEmailTextBox.setText(Email);
                }
                
            }
            sc.close();
        }
        catch (Exception e)
        {
            
        }

    }
