    private void PMEditProfileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PMEditProfileButtonActionPerformed
    PasswordValidation();
    
    if(checkpassword == true){
        
    try
    {
        FileInputStream fstream = new FileInputStream("ProductManager.txt");
        BufferedReader br = new BufferedReader(new InputStreamReader(fstream));
        String line = "";
        StringBuilder data = new StringBuilder();
                while ((line = br.readLine()) != null)
                {
                    String values[] = line.split(",");
                    if (values.length>0) {
                        if (values[0].equals(PMIDTextBox.getText())) {
                            String dataRow = PMIDTextBox.getText() + "," + PMUsernameTextBox.getText() + "," + PMINTextBox.getText() + ","
                            + PMPasswordTextBox.getText() + "," + PMContactTextBox.getText() + "," + PMEmailTextBox.getText() + "," + "Product Manager" + "," + "Active" + ",";
                            data.append(dataRow);
                            data.append("\n");
                        }
                        else
                        {
                            data.append(line);
                            data.append("\n");
                        }
                    }
                }
                fstream.close();
                FileWriter fw = new FileWriter("ProductManager.txt");
                BufferedWriter bw = new BufferedWriter(fw);
                bw.write(data.toString());
                bw.close();
                fw.close();
                JOptionPane.showMessageDialog(null,"Profile Modified.");
            }
            catch(IOException e)
            {
            } 
    }
        
    }//GEN-LAST:event_PMEditProfileButtonActionPerformed

